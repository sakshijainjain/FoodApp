public void showDataInTable()
{
    try{    
    ArrayList<EmpPojo> empList=EmpDao.getAllData();
    if(empList.isEmpty()==true)
                JOptionPane.showMessageDialog(null,"sorry no emp present","error!",JOptionPane.INFORMATION_MESSAGE);
    else{
    String[] rows=new String[4];
    DefaultTableModel model=(DefaultTableModel)jTab.getModel();
                for(EmpPojo p:empList){
                    rows[0]=p.getEmpId();
                    rows[1]=p.getEmpName();
                    rows[2]=p.getJob();
                    rows[3]=p.getSalary();
                   
                    model.addRow(rows);
                } 
    }
}  
    catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB errors","error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
}
