    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(txtEmpId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Enter Emp id","Error",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else{
            try{
                ArrayList<EmpPojo> empList=EmpDao.getAllDataById(txtEmpId.getText());
                  if(empList.isEmpty()==true)
                    JOptionPane.showMessageDialog(null,"sorry no detail is present","error!",JOptionPane.INFORMATION_MESSAGE);
                  else
                  { 
                      
                      String[] rows=new String[4];
                       DefaultTableModel model=(DefaultTableModel)jTab.getModel();
                       for(EmpPojo p:empList){
                              rows[0]=p.getEmpId();
                              rows[1]=p.getEmpName();
                              rows[2]=p.getJob();
                              rows[3]=p.getSalary();
                              model.addRow(rows);
                              
                         
                       }
                 
                       txtEmpId.setText("");
                  }
            }
               catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB errors","error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

