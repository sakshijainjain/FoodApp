    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
      int row= jTab.getSelectedRow();
      DefaultTableModel model=(DefaultTableModel)jTab.getModel();
     
      String empid=jTab.getValueAt(row, 0).toString();
      try{
      boolean status=EmpDao.deleteEmp(empid);
         
      if(status){
            
           model.removeRow(row);
           JOptionPane.showMessageDialog(null,"emp delete successfully","success!",JOptionPane.INFORMATION_MESSAGE);
      
      }
           else
           JOptionPane.showMessageDialog(null,"sorry emp cannot delete","error!",JOptionPane.INFORMATION_MESSAGE);
      }
     
           catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB errors","error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDelActionPerformed

